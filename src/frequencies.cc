const Channel&
channel_by_number(const std::vector<Channel>& band, int channel)
{
        for (auto& ch : band) {
                if (ch.channel == channel) {
                        return ch;
                }
        }
        throw std::runtime_error((boost::format("unknown channel %d") % channel).str());
}
